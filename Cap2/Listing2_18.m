% Archivo: calcularAreaCirculo.m
function area = calcularAreaCirculo(radio)
% CALCULARAREACIRCULO Calcula el área de un círculo.
%   AREA = CALCULARAREACIRCULO(RADIO) devuelve el área
%   de un círculo con el RADIO especificado.
    area = pi * radio^2;
end

